
public class ClaseSwitch {

	public static void main(String[] args) {
		
		String opcion = "ASDASD";
		
		switch(opcion) {
		case "A" :
			System.out.println("Eligio la opcion A");
			break;
		case "B":
			System.out.println("Eligio la opcion B");
			break;
		default :
			System.out.println("No eligio ninguna opcion correcta");
			break;
		}
		
		

	}

}
